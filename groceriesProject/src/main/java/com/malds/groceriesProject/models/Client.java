package com.malds.groceriesProject.models;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;

import lombok.Data;

@Data
@DynamoDBTable(tableName = "Client")
public class Client {

    /**
     * DynamoDBAttribute: clientID. type: String Hash Key
     */
    @DynamoDBHashKey
    @DynamoDBAutoGeneratedKey
    private String clientID;

    /**
     * DynamoDBAttribute: email. type: String
     */
    @DynamoDBAttribute
    private String email;

    /**
     * DynamoDBAttribute: firstName. type: String
     */
    @DynamoDBAttribute
    private String firstName;

    /**
     * DynamoDBAttribute: lastName. type: String
     */
    @DynamoDBAttribute
    private String lastName;

    /**
     * DynamoDBAttribute: gender. type: String
     */
    @DynamoDBAttribute
    private String gender;

    /**
     * DynamoDBAttribute: dateOfBirth. type: String
     */
    @DynamoDBAttribute
    private String dateOfBirth;

    /**
     * DynamoDBAttribute: zipcode. type: String
     */
    @DynamoDBAttribute
    private String zipcode;

    /**
     * Client default constructor.
     */
    public Client() {
        this.clientID = null;
        this.email = null;
        this.firstName = null;
        this.lastName = null;
        this.gender = null;
        this.dateOfBirth = null;
        this.zipcode = null;
    }
}
